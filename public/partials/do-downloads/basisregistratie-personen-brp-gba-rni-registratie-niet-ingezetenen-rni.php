    <p>Iemand die voor een periode van 4 maanden of langer in Nederland woont, wordt ingeschreven in de <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr>. Dit noemen we ingezetenen. Naast ingezetenen zijn er ook niet-ingezetenen. Dit zijn personen die niet of korter dan 4 maanden in Nederland wonen. Zij worden niet in de <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> ingeschreven.Niet-ingezetenen kunnen een relatie hebben met Nederlandse overheidsinstellingen. Bijvoorbeeld mensen die tijdelijk in Nederland wonen of werken. Of mensen die in Nederland hebben gewoond en die nu in het buitenland van hun pensioen genieten. Overheidsinstellingen houden nu elk hun eigen registratie bij van niet-ingezetenen met wie zij te maken hebben. In de Registratie Niet-ingezetenen (<abbr title="Registratie Niet Ingezetenen">RNI</abbr>) worden straks alle gegevens centraal en op dezelfde manier vastgelegd. Afnemers kunnen gegevens uit de <abbr title="Registratie Niet Ingezetenen">RNI</abbr> verstrekt krijgen voor de uitvoering van hun publieke taak. De <abbr title="Registratie Niet Ingezetenen">RNI</abbr> is bedoeld voor organisaties met een publieke taak die niet-ingezetenen in hun doelgroep hebben; denk aan de <abbr title="Sociale Verzekeringsbank">SVB</abbr>, <abbr title="Uitvoeringsinstituut Werknemersverzekeringen">UWV</abbr>, <abbr title="Dienst Uitvoering Onderwijs">DUO</abbr> en Belastingdienst. Ook gemeenten kunnen afnemer zijn. De defitieve invoeringsdatum van de <abbr title="Registratie Niet Ingezetenen">RNI</abbr> is afhankelijk van de inwerkingtreding van de nieuwe wet basisregistratie personen.</p>	<h2>Gerealiseerde releases</h2>
