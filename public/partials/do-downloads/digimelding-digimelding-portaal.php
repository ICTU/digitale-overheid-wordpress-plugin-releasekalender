<p>Het portaal is een webapplicatie waarmee u als afnemerorganisatie terugmeldingen kunt doen. Het geeft uw medewerkers toegang tot Digimelding. Via het webportaal van Digimelding meldt een ambtenaar aan de Basisregistratie dat hij vermoedt dat bepaalde gegevens niet kloppen. Die melding komt vervolgens automatisch binnen bij de juiste Basisregistratie.   Deze release is niet bedoeld voor implementatie in productieomgevingen, maar alleen voor een praktijkproef met een selecte groep deelnemers.</p><h4>Afhankelijkheden</h4>
