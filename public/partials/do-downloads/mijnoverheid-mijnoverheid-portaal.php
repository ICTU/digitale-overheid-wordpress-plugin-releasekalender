    <p>MijnOverheid is het portaal dat burgers overzicht en toegang biedt tot hun berichten van, zaken met en persoonlijke gegevens bij de overheid. Het portaal MijnOverheid biedt toegang tot de 3 functies: Berichtenbox Lopende Zaken Persoonlijke Gegevens.Daarnaast bevat het portaal een aantal ondersteunende functies, zoals registratie, profielbeheer en sms- en emailnotificatie. Het portaal MijnOverheid is onderdeel van het overkoepelende portaal Overheid.nl en is bedoeld voor alle overheidsorganisaties die zijn aangesloten op een van de 3 functies van MijnOverheid: Berichtenbox, Persoonlijke Gegevens en/of Lopende Zaken.    </p>	<h2>Gerealiseerde releases</h2>
