<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p><abbr title="Digitale Identiteit">DigiD</abbr> is het digitale authenticatiesysteem voor overheidsorganisaties en publieke dienstverleners. Met <abbr title="Digitale Identiteit">DigiD</abbr> kunnen zij online de identiteit van burgers vaststellen. <abbr title="Digitale Identiteit">DigiD</abbr> zorgt ervoor dat overheidsorganisaties en publieke dienstverleners betrouwbaar zaken kunnen doen met burgers via hun website. Logt een burger met zijn persoonlijke <abbr title="Digitale Identiteit">DigiD</abbr> in dan krijgt hij via <abbr title="Digitale Identiteit">DigiD</abbr> het Burgerservicenummer (<abbr title="Burgerservicenummer">BSN</abbr>) teruggekoppeld. Zo weten zij precies met wie ze te maken hebben en kunnen ze direct nagaan welke informatie ze al hebben over een persoon.</p>    <p>
