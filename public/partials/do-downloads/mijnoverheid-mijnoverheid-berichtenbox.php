    <p>De Berichtenbox is één van de 3 functies van MijnOverheid. Via de Berichtenbox ontvangen burgers hun persoonlijke elektronische berichten van de overheid. De Berichtenbox is te beschouwen als een elektronische opvolger van de papieren post.</p>
<p>De Berichtenbox is bedoeld voor alle overheidsorganisaties die berichten aan burgers versturen.</p>	<h2>Gerealiseerde releases</h2>
