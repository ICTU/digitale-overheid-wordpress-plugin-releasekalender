<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>Het digitaal Ondernemersplein is de plek waar de dienstverlening van de overheid integraal voor de ondernemer wordt ontsloten. Onder dienstverlening worden zowel informatie-uitwisseling als transacties en (digitale) interactie verstaan. </p>
<p>Hierbij wordt uitgegaan van het ‘no-wrong-door’ principe. Dit betekent dat het plein een toegang is voor alle ondernemerszaken, maar niet dat al deze zaken/transacties hier ook daadwerkelijk zullen plaatsvinden. Ondernemers hoeven daardoor slechts op één locatie hun informatie te halen, daarnaast kunnen transacties met betrekking tot ondernemen bij overheidspartijen via ondernemersplein.nl gemakkelijk uitgevoerd worden.</p>    <p>
        <em>Datum laatste wijziging: 19 augustus 2015</em>
    </p>
