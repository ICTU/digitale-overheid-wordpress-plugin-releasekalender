<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>De website Antwoord voor bedrijven is per 15 september 2014 opgegaan in Ondernemersplein.nl. Op deze website is alle informatie van de (semi-)overheid te vinden die een ondernemer nodig heeft. De redactie van Antwoord voor bedrijven levert hiervoor informatie over wetten, regels en subsidies aan. Het Ondernemersplein is een initiatief van het ministerie van Economische Zaken in samenwerking met de Kamer van Koophandel, de Rijksdienst voor Ondernemend Nederland en de Belastingdienst.</p>    <p>
        <em>Datum laatste wijziging: 04 augustus 2016</em>
    </p>
