    <h2 id="omschrijving">Omschrijving</h2>
    <p>Digikoppeling bestaat uit, door het College Standaardisatie vastgestelde, koppelvlakstandaarden. Die standaarden bevatten logistieke afspraken om berichten juist te adresseren, leesbaar en uitwisselbaar te maken en veilig en betrouwbaar te verzenden.</p>
<p>Digikoppeling gaat over de 'envelop' en voert uit wat daar op staat. De gegevens gaan dan naar de juiste overheidsorganisatie en naar het juiste adres. Aangetekend of juist niet, versleuteld, als herhaalde aanbieding, of retour-afzender. De inhoud van het bericht in de envelop is voor de geadresseerde.</p>
<p>Digikoppeling onderkent twee hoofdvormen van berichtenverkeer: bevragingen en meldingen. De vorm van het berichtenverkeer bepaalt welke koppelvlakstandaard moet worden geïmplementeerd (in de meeste gevallen zal een organisatie aan beide behoefte hebben)</p>
<p>Uitgangspunt van Digikoppeling is dat een organisatie op één plaats in de eigen <abbr title="Informatie en Communicatie Technologie">ICT</abbr>-infrastructuur een koppelpunt volgens Digikoppeling inricht.</p>
<p>Digikoppeling biedt een aantal ondersteunende voorzieningen voor de implementatie: Serviceregister, Compliancevoorzieningen en CPA-creatievoorziening.</p>    <p>
