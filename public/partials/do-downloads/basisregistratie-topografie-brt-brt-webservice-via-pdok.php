		<p>De <abbr title="Basisregistratie Topografie">BRT</abbr> Achtergrondkaart op <abbr title="Publieke Dienstverlening op de Kaart">PDOK</abbr> is de kaartlaag waarop locatiegebonden content wordt afgebeeld. De achtergrondkaart moet ervoor zorgen dat de eindgebruiker zich kan orienteren tijdens het gebruik van geografische applicaties. De <abbr title="Basisregistratie Topografie">BRT</abbr> Achtergrondkaart is afgeleid uit TOP10NL uit de Basisregistratie Topografie (<abbr title="Basisregistratie Topografie">BRT</abbr>) met de straatnamen uit de Basisregistraties Adressen en Gebouwen (<abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr>).</p>
<p>De <abbr title="Basisregistratie Topografie">BRT</abbr> Achtergrondkaart wordt aangeboden in 3 visualisaties:</p>
<ul><li>standaard</li>
<li>grijs</li>
<li>pastel</li>
