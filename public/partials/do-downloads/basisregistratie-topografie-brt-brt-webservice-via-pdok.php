	    <ul>
	        <li>Gerealiseerd op 31 december 2012</li>
		    <li>Verwijzing: <a class="external_link" href="https://www.pdok.nl/nl/service/wmts-top25raster">TOP25raster</a></li>
<li>Releasestatus: Gerealiseerd</li>
		    	    </ul>
		<p>TOP25raster geeft het kaartbeeld van de topografische kaartserie 1:25 000. Door een geautomatiseerd proces vanuit het geografisch basisbestand 1:10 000 resulteert dit in een product met de volgende kenmerken: Een consistente kleurstelling voor het gehele land; Door de directe relatie met het geografisch basisbestand, TOP10NL, kan het onderhoud beter worden gegarandeerd. TOP25raster zal in een cyclus van een jaar worden geactualiseerd. Voor analyses of applicatie-ontwikkeling in GIS, CAD en Desktop Mapping waarbij het niet nodig is om wijzigingen in het kaartbeeld aan te brengen, maar waarbij de kaart als ondergrond en referentie functioneert, zijn rasterbestanden een goed alternatief. De TOPraster-producten bieden hiervoor de ideale oplossing. Deze bestanden worden softwarematig afgeleid uit de vectorbestanden.</p>		<h4>Afhankelijkheden</h4>
