    <p><abbr title="Digitale Identiteit">DigiD</abbr> is het digitale authenticatiesysteem voor de overheid en publieke dienstverleners. <abbr title="Digitale Identiteit">DigiD</abbr> zorgt ervoor dat u betrouwbaar zaken kunt doen met burgers via uw website. Logt iemand met zijn <abbr title="Digitale Identiteit">DigiD</abbr> in op uw website? Dan krijgt u via <abbr title="Digitale Identiteit">DigiD</abbr> het burgerservicenummer teruggekoppeld. Zo weet u precies met wie u te maken hebt, en kunt u direct nagaan welke informatie u al hebt over een persoon.</p>
    <p>Sinds 2012 biedt <abbr title="Digitale Identiteit">DigiD</abbr> een faciliteit voor Eenmalig Inloggen. Daarmee hoeft de burger niet nogmaals in te loggen als deze wisselt tussen websites van aangesloten dienstverleners.</p>
