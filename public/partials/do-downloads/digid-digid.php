    <h3 id="digid-4-8-wachtwoordherstel"><abbr title="Digitale Identiteit">DigiD</abbr> 4.8 "Wachtwoordherstel"</h3>
    <ul>
      <li>Gerealiseerd op 21 september 2015</li>
      <li>Releasestatus: Gerealiseerd</li>
      <li>Aandachtspunten:
        <p>Gebruiksvriendelijkheid en toegankelijkheid</p>
      </li>
    </ul>
    <p>•&nbsp;De functie wachtwoordherstel via <abbr title="elektronische mail">e-mail</abbr> en sms wordt met deze release automatisch ingesteld voor iedereen die de extra controle via sms heeft geactiveerd en een geregistreerd en gecontroleerd <abbr title="elektronische mail">e-mail</abbr>adres bezit. Dit betekent dat deze functionaliteit niet meer aan- of uitgezet kan worden. Tijdens het aanvraagproces van een nieuw wachtwoord kan de burger altijd kiezen voor wachtwoordherstel middels een brief.</p>
    <p>•&nbsp;Tijdsaanduidingen binnen <abbr title="Digitale Identiteit">DigiD</abbr> worden aangevuld met de woorden “Nederlandse tijd”.&nbsp;•&nbsp;<abbr title="Digitale Identiteit">DigiD</abbr> verhoogt de absolute sessie-timeout voor een sessie met eenmalig inloggen van 120 minuten naar 180 minuten, zodat gebruikers langer de tijd hebben om hun zaken digitaal te regelen. Voor organisaties met webdiensten die gebruik maken van eenmalig inloggen is het van belang dat dit zij dit ook aanpassen in de webdienstconfiguratie. Let op, de sessie-timeout van 15 minuten (bij inactiviteit) blijft van kracht.</p>
