<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>Digimelding is de generieke oplossing voor het melden van onjuistheden in basisregistraties. Wanneer overheidsorganisaties gebaseerd op hun eigen informatie vermoeden dat een gegeven in een basisregistratie niet (meer) klopt, kunnen ze dat met Digimelding melden aan de bronhouder. Die onderzoekt de melding vervolgens en wijzigt als dat nodig is de gegevens in de basisregistratie. Digimelding zorgt dat meldingen op een uniforme wijze gebeuren en bij de juiste bronhouder terecht komen.</p>    <p>
        <em>Datum laatste wijziging: 07 december 2016</em>
    </p>
