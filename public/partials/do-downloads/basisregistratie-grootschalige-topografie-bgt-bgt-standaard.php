	    <ul>
	        <li>Gerealiseerd op 01 april 2014</li>
		    <li>Verwijzing: <a class="external_link" href="http://imgeo.geostandaarden.nl/">Objectenhandboek</a></li>
<li>Releasestatus: Gerealiseerd</li>
		    	    </ul>
		<p>Het objectenhandboek geeft (foto-)voorbeelden waarop voorkomende objecten en hun begrenzing zijn weergegeven.</p>
<p>Het objectenhandboek is geen onderdeel van de regelgeving maar een hulpmiddel in het gebruik en visueel maken van onderdelen van het informatiemodel. Het objectenhandboek geeft inzicht in de manier waarop topografische objecten zijn gemodelleerd in de <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr> en het IMGeo. Dit gebeurt in eerste instantie aan de hand van fotovoorbeelden waarop de begrenzing van objecten en zoveel mogelijk de objecten zelf zijn weergegeven. Dit is zowel van belang bij de inwinning als bij het gebruik van de datasets.</p>
<p>Het objectenhandboek is digitaal beschikbaar. Vanuit de digitale toepassing kunt u zelf een voor u relevante selectie van objecten selecteren en als .pdf downloaden.</p>		<h4>Afhankelijkheden</h4>
