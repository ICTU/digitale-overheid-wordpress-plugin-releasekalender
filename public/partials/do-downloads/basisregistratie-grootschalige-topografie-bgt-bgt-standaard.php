		    	    <h3 id="berichtenstandaard-stuf-geo-imgeo-1-2">Berichtenstandaard <abbr title="Standaard Uitwisselings Formaat">StUF</abbr>-Geo IMgeo 1.2</h3>
	    <ul>
	        <li>Gerealiseerd op 01 januari 2015</li>
		    <li>Verwijzing: <a class="external_link" href="http://www.geonovum.nl/documenten/toelichting-bgt-berichtenverkeer-versie-12-wat-heb-je-nodig">Berichtenstandaard <abbr title="Standaard Uitwisselings Formaat">StUF</abbr>-Geo IMgeo 1.2</a></li>
<li>Releasestatus: Gerealiseerd</li>
		    	<li>Aandachtspunten: <p>Het aanleveren van initiele leveringen en mutaties conform de in productie zijnde Berichtstandaard versie 1.1.1 blijft gedurende 2015 mogelijk.</p>
<p>De technische wijzigingen hebben alleen betrekking hebben op automatisch berichtenverkeer. Toepassing van het XSD schema van versie 1.2 is uitsluitend nodig voor het vooraankondigingsbericht. Voor alle andere berichten blijft XSD schema 1.1.1 van kracht.</p>
<p>Verder zijn t.b.v. automatisch berichtenverkeer definitieve WSDL's gepubliceerd voor het koppelvlak bronhouder-<abbr title="Sociale Verzekeringsbank">SVB</abbr> en het koppelvlak <abbr title="Sociale Verzekeringsbank">SVB</abbr>-<abbr title="Landelijke Voorziening">LV</abbr>.</p></li>
		    	    </ul>
		<p>De berichtenstandaard is qua indeling en redactie aangepast met het oog op toekomstige wettelijke verankering.</p>
<p>Verder worden inhoudelijke vereenvoudigingen doorgevoerd i.v.m. de overeengekomen complexiteitsreductie van het berichtenverkeer:</p>
<ul><li>berichten tbv goedkeuringen komen te vervallen</li>
<li>vooraankondigingen zijn ter informatie, niet blokkerend</li>
<li>vereenvoudigde procesgang t.g.v. vervallen goedkeuring</li>
<li>de 4 uiteenlopende wijzen waarop de bronhouder met <abbr title="Sociale Verzekeringsbank">SVB</abbr> kon actualiseren, is teruggebracht tot 1 oplossing om te actualiseren.</li>
</ul>
<p>Verder is de invulling van de logistieke laag d.m.v. Digikoppeling WUS 3.0 expliciet beschreven.</p>
<p>Tot slot zijn onderdelen die betrekking hebben op binnengemeentelijke (horizontale) uitwisseling tussen <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr> en BOR verwijderd uit de berichtenstandaard, aangezien deze buiten de scope van de wet <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr> vallen. Geonovum publiceert deze voortaan in separate documenten.</p>
