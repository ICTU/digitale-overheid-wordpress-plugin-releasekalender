    <p>De gegevenscatalogus beschrijft regels voor inwinning, beheer en  uitwisseling van objecten die tezamen de Basisregistratie Grootschalige Topografie vormen. Gebruikers vinden in deze gegevenscatalogus informatie over het doel, het gebruik, de inhoud en de kwaliteit van de <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr>.</p>
<p>De standaard bestaat uit de volgende producten:</p>
<ul><li>Gegevenscatalogus <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr> / IMgeo</li>
<li>Berichtenstandaard (schema's, functioneel ontwerp, codelijsten, handrijking)</li>
<li>Objectenhandboek</li>
<li>Visualisatie richtlijn (handreiking, symbolenset, SLD, templates)</li>
</ul>
<p>Er zijn ook ondersteunende instrumenten beschikbaar:</p>
<ul><li>Voorbeeldbestanden en -berichten</li>
<li>Werkafspraken</li>
<li>Validator</li>
<li>Release notes</li>
