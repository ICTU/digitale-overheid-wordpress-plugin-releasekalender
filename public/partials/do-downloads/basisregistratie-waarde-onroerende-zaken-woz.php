    <h2 id="omschrijving">Omschrijving</h2>
    <p></p>
<p>De Basisregistratie Waarde Onroerende Zaken (<abbr title="Waarde Onroerende Zaken">WOZ</abbr>) maakt het mogelijk dat de in de <abbr title="Waarde Onroerende Zaken">WOZ</abbr>-beschikking vastgestelde <abbr title="Waarde Onroerende Zaken">WOZ</abbr>-waarde door alle overheidsorganisaties, die daarvoor een wettelijke regeling hebben, gebruikt kan worden.</p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
    </p>
