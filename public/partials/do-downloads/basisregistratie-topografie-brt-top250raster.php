    <p>TOP250raster geeft het kaartbeeld van de Wegenkaart van Nederland, schaal 1:250.000. Dit bestand wordt softwarematig gegenereerd uit de vectorbestanden. TOP250raster is geschikt voor minder gedetailleerde toepassingen, maar ook als index voor TOP25raster en TOP50raster.de topografische kaartserie 1:50 000. Door een geautomatiseerd proces vanuit het geografisch basisbestand 1:50 000 resulteert dit in een product met de volgende kenmerken: Een consistente kleurstelling voor het gehele land; Door de directe relatie met TOP250vector, kan het onderhoud beter worden gegarandeerd. TOP250raster wordt in een cyclus van twee jaar geactualiseerd.Voor analyses of applicatie-ontwikkeling in GIS, CAD en Desktop Mapping waarbij het niet nodig is om wijzigingen in het kaartbeeld aan te brengen, maar waarbij de kaart als ondergrond en referentie functioneert, zijn rasterbestanden een goed alternatief. De TOPraster-producten bieden hiervoor de ideale oplossing. Deze bestanden worden softwarematig afgeleid uit de vectorbestanden.</p>	<h2>Gerealiseerde releases</h2>
