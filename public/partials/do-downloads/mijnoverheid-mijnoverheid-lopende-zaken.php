    <p>Lopende Zaken is één van de 3 functies van MijnOverheid. Het biedt burgers een overzicht van, toeleiding naar en toegang tot hun zaken met de overheid. Lopende Zaken toont een korte beschrijving van de zaak, laat zien wat de actuele status is en verwijst voor detailinformatie naar het integrale zaakdossier bij de betreffende overheidsorganisatie.</p>
<p>MijnOverheid Lopende Zaken is bedoeld voor alle overheidsorganisaties die zaakgerichte werkzaamheden uitvoeren.</p>	<h2>Gerealiseerde releases</h2>
