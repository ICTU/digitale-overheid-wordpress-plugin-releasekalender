    <h2 id="omschrijving">Omschrijving</h2>
    <p><abbr title="Elektronische Herkenning">eHerkenning</abbr> is een gestandaardiseerd inlogsysteem, waarmee organisaties hun diensten veilig online toegankelijk maken voor bedrijven, ambtenaren en consumenten. Gebruikers loggen met een <abbr title="Elektronische Herkenning">eHerkenning</abbr>smiddel in op de webdienst om vervolgens hun zaken veilig online te regelen. Veilig, betrouwbaar en eenvoudig.</p>
    <p><abbr title="Elektronische Herkenning">eHerkenning</abbr> is fundament voor Idensys</p>
    <p>De overheid werkt samen met het bedrijfsleven aan een nieuwe standaard voor toegang tot online dienstverlening die voortbouwt op het stelsel van <abbr title="Elektronische Herkenning">eHerkenning</abbr>: Idensys (voorheen eID Stelsel). <abbr title="Elektronische Herkenning">eHerkenning</abbr> biedt een stevige basis waarop verder kan worden ontwikkeld. Organisaties die een snelle start willen maken metÂ Idensys kunnen nu al aansluiten op <abbr title="Elektronische Herkenning">eHerkenning</abbr>. Organisaties die aansluiten of al aangesloten zijn op <abbr title="Elektronische Herkenning">eHerkenning</abbr> migreren straks mee naar Idensys.</p>
