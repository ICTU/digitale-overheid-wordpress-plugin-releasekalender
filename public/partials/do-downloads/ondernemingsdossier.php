    <h2 id="omschrijving">Omschrijving</h2>
    <p>Het Ondernemingsdossier is een nieuwe manier van samenwerken en informatie delen tussen ondernemers en overheden met als doel de regeldruk te verminderen met minimaal 15% voor de betrokken ondernemers.</p>
<p>In het Ondernemingsdossier komt alle informatie die een onderneming en de toezichthoudende of vergunningverlenende overheden met elkaar uitwisselen samen. Het bedrijf dat de informatie in het dossier plaatst, maakt haar activiteiten inzichtelijk voor zichzelf en de verschillende overheden die zijn aangesloten op het Ondernemingsdossier. De ondernemer bepaalt welke overheden toegang hebben tot zijn dossier. Omgekeerd kunnen overheden via het dossier informatie geven, vragen stellen of de ondernemer op zijn verplichtingen wijzen. Ook het aanvragen en verstrekken van vergunningen en het doen van meldingen, kan digitaal via het dossier worden geregeld.</p>
<p>Invoering draagt voor bedrijfsleven en overheden bij aan minder regeldruk voor betrokken bedrijven en een vermindering van de uitvoeringskosten voor betrokken overheden. Het resultaat: meer tijd om te ondernemen, betere naleving van regels en vereenvoudiging van het toezicht.</p>    <p>
        <em>Datum laatste wijziging: 13 februari 2015</em>
    </p>
