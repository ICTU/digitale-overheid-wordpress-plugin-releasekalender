    <h2 id="omschrijving">Omschrijving</h2>
    <p>Bedrijven in Nederland moeten jaarlijks verplichte financiële rapportages aanleveren aan verschillende partijen, waaronder banken en overheden. Dat kost tijd, geld en energie. Het eenmalig inrichten van de bedrijfsadministratie volgens Standard Business Reporting (<abbr title="Standard Business Reporting">SBR</abbr>) zorgt voor efficiënt hergebruik van gegevens. Met <abbr title="Standard Business Reporting">SBR</abbr> wordt rapporteren beter, sneller en eenvoudiger.</p>
<p><abbr title="Standard Business Reporting">SBR</abbr> is de nationale standaard voor de digitale uitwisseling van alle bedrijfsmatige rapportages. Samen met organisaties uit de markt, zoals accountants, boekhouders, softwareleveranciers en banken heeft de Nederlandse overheid <abbr title="Standard Business Reporting">SBR</abbr> ontwikkeld. Met <abbr title="Standard Business Reporting">SBR</abbr> worden de gegevens in de bedrijfsadministratie eenmalig op een standaard manier vastgelegd. De gegevens zijn eenvoudig te hergebruiken voor verschillende rapportages aan overheidsinstellingen en een aantal banken, doordat de bedrijfsadministratie het bronbestand is. Zo hebben ondernemers en intermediairs minder werk aan het samenstellen en aanleveren van verschillende (verplichte) rapportages.</p>    <p>
        <em>Datum laatste wijziging: 26 mei 2016</em>
    </p>
