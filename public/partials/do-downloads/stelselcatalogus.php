<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Stelselcatalogus is de primaire kennisbron voor informatie over welke gegevens er in het Stelsel beschikbaar zijn en wat deze gegevens betekenen (semantiek). Met die informatie kunnen gebruikers gegevens uit de basisregistratie(s) makkelijk inpassen in hun eigen werkprocessen. De definities in de Stelselcatalogus zijn overgenomen uit de verschillende basisregistraties. De beheerders van de basisregistraties onderhouden de definities zelf.</p>    <p>
        <em>Datum laatste wijziging: 20 april 2016</em>
    </p>
