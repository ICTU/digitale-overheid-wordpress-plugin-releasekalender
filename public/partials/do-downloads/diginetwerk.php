    <h2 id="omschrijving">Omschrijving</h2>
    <p>Diginetwerk is een afsprakenstelsel voor het koppelen van besloten netwerken van de overheid. Via deze gekoppelde netwerken kunnen overheidsorganisaties onderling gegevens uitwisselen. Door het besloten karakter is Diginetwerk een veiliger alternatief dan het open internet voor het uitwisselen van gegevens tussen overheidsorganisaties. Via Diginetwerk heeft u connectiviteit met alle aangesloten overheidsorganisaties via één koppeling. </p>    <p>
        <em>Datum laatste wijziging: 18 juli 2016</em>
