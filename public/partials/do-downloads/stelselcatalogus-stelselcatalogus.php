<p>Op 13 mei 2013 is de bètarelease van de Stelselcatalogus live gegaan. Met het ‘live’ gaan van het instrument Stelselcatalogus is het eerste deel gerealiseerd van de langetermijnvisie Stelselcatalogus zoals dat is vastgesteld door de stuurgroep Stelselcatalogus. Het resultaat is de vervanging van het instrument Stelselcatalogus op basis van het federatieve concept.</p>
<p>De bètarelease heeft tot doel om aan de hand van een aantal concrete situaties (business cases) kansrijke toepassingen van de Stelselcatalogus te beproeven. Het projectteam doet dit in samenwerking met een aantal gebruikers in de periode mei t/m augustus</p>
<p>Op basis van de uitkomsten deze activiteiten zal de Stelselcatalogus worden aangepast waardoor de huidige bètarelease kan overgaan in de definitieve release. Dit zal in de periode september/oktober worden gerealiseerd.</p><h4>Afhankelijkheden</h4>
