    <p>De Stelselcatalogus is de primaire kennisbron voor informatie over welke gegevens er in het Stelsel beschikbaar zijn en wat deze gegevens betekenen (semantiek). Met die informatie kunnen gebruikers gegevens uit de basisregistratie(s) makkelijk inpassen in hun eigen werkprocessen. De definities in de Stelselcatalogus zijn overgenomen uit de verschillende basisregistraties. De beheerders van de basisregistraties onderhouden de definities zelf.</p>	<h2>Gerealiseerde releases</h2>
		    	    <h3 id="stelselcatalogus-2-3">Stelselcatalogus 2.3</h3>
	    <ul>
	        <li>Gerealiseerd op 01 december 2014</li>
		    <li>Verwijzing: <a class="external_link" href="http://www.e-overheid.nl/onderwerpen/stelselinformatiepunt/stelsel-van-basisregistraties/stelselvoorzieningen/stelselcatalogus">Stelselcatalogus 2.3</a></li>
<li>Releasestatus: Gerealiseerd</li>
		    	    </ul>
		<p>De release bestaat uit twee logische eenheden:• PHP applicatie (presentatie via webpagina’s)• RDF Tripplestore (databeheer)</p>
<p>Werking• De PHP applicatie werkt CMS onafhankelijk en  toont de informatie over gegevenssoorten en voor-komens van begrippen via webpagina’s (HTML) aan de gebruiker.• De informatie over de gegevenssoorten en begrippen wordt door de PHP-applicatie via de RDF-interface uit de RDF-store gehaald.• De RDF-store is gevuld met de metagegevens over de gegevenssoorten en de voorkomens van de begrippen in de basisregistraties. Deze metagegevens worden ingelezen op basis van gegevensbe-standen die door de beheerders van de basisregistraties worden aangeleverd. </p>		<h4>Afhankelijkheden</h4>
