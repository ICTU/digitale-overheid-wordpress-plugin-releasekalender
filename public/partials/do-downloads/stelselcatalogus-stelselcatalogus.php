<p>Ter stimulering en verbetering van het gebruik zijn er nieuwe  functionaliteiten doorgevoerd. Dit betreft:</p>
<ul><li>het toevoegen van de nieuwe metagegevens “toegankelijkheid” en “gebruiksvoorwaarden”;</li>
<li>de mogelijkheid om de webinterface vrij ter beschikking te stellen voor hergebruik door andere catalogi;</li>
<li>uitbreiding van de linked data formaat die worden ondersteund;</li>
<li>koppeling met de “Kenniskluis” van het Kadaster.</li>
</ul>
<p>Op basis van de gebruikerstesten, meldingen en opmerkingen van gebruikers n.a.v. release 2.1, zijn er ook een aantal aanpassingen in de bestaande functionaliteit doorgevoerd. De aanpassingen hebben betrekking op:</p>
<ul><li>het  verduidelijken van de context van authentieke gegevens. De relatie tussen  (authentieke) gegevens en begripsvoorkomens worden expliciet weergegeven;</li>
<li>het filteren op authentieke gegevens bij vergelijken van begrippen en gegevens;</li>
<li>het filteren op begrippen en het tonen van toelichtingen in de interactieve stelselplaat;</li>
<li>het verwijzen naar PDF-catalogi wanneer gegevens niet in stelselcatalogus staan;</li>
<li>het terug “bladeren” tussen de pagina’s dient eenduiding  te verlopen, zodat het voor de gebruiker duidelijker is waar hij zich in de stelselcatalogus bevindt;</li>
<li>technische aanpassingen waardoor de stabiliteit, werking en duurzaamheid van de stelselcatalogus wordt verbeterd.</li>
