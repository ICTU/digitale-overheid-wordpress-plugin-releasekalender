    	    		    <h3 id="stelselcatalogus-3-0">Stelselcatalogus 3.0</h3>
		    <p class="releasedatum">01 januari 2017, 0-2 maanden marge</p>
<ul>
		    	<li>Releasestatus: Verwacht</li>
</ul>
		    <p>Speerpunten:</p>
<p>1. Usability. De huidige interface is ingewikkeld en onduidelijk. Veel voorkennis is vereist en het nodigt niet uit tot frequent gebruik. Een gebruikersonderzoek is noodzakelijk om te inventariseren welke veranderingen er moeten worden doorgevoerd. </p>
<p>2. Uitbreiding content. Ambitie is om van de Stelselcatalogus de centrale catalogus te maken ten aanzien van alle gegevensbronnen van de overheid. Denk hierbij aan de verschillende domeinen (Laan van de Leefomgeving, Loonaangifteketen, Zorg, Onderwijs etc.) en beschikbare open data sets. De <abbr title="Samenwerkende Catalogi">SC</abbr> moet zodanig in elkaar zitten dat nieuwe gegevensbronnen makkelijk zijn toe te voegen zonder dat het het gebruiksgemak compromitteert.</p>
<p>3. Link met LiDO (Wettenbank). In de <abbr title="Samenwerkende Catalogi">SC</abbr> wordt de wettelijke context ge-toond van de begrippen in de basisregistraties. Andersom dient een link te worden gelegd tussen de Wettenbank van KOOP en de <abbr title="Samenwerkende Catalogi">SC</abbr> zodat een gebruiker van de Wettenbank meteen duiding krijgt van de betekenis van een begrip dat in de wetstekst wordt genoemd.</p>
