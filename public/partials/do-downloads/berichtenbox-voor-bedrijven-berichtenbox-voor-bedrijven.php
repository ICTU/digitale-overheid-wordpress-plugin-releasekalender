<p>5.    Tonen laatste login. Na het inloggen wordt de mededeling gegeven wanneer de gebruiker er voor het laatst is geweest. Dit om eventueel misbruik (anderen die onbevoegd hebben ingelogd in de tussentijd) eerder te kunnen signaleren.6.    Liggend streepje toestaan in organisatienaam.</p>		<h4>Afhankelijkheden</h4>
