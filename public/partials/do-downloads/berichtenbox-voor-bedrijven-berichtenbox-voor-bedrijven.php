		<p>Aanmaak nieuwe berichtenboxen vanuit het ondernemingsdossierAls een ondernemer een ondernemersdossier wil aanmaken en dit actief wil gebruiken om vergunningen aan te vragen, dan heeft hij ook een berichtenbox nodig. De berichtenbox  is het standaardkanaal voor aanvragen vanuit het Ondernemingsdossier. Om de ondernemer het zo makkelijk mogelijk te maken is het aanmaakproces van een berichtenbox daarom geïntegreerd in het aanmaakproces van een ondernemingsdossier. Nu we voor Nederlandse ondernemers volledig overgaan op <abbr title="Elektronische Herkenning">eHerkenning</abbr> moet dat proces aangepast worden. Dat vergt ook aanpassingen in de berichtenbox.</p>		<h4>Afhankelijkheden</h4>
