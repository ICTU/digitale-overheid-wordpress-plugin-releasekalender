	    <ul>
	        <li>Gerealiseerd op 15 september 2013</li>
		    <li>Releasestatus: Gerealiseerd</li>
		    	    </ul>
		<p>1.    Mogelijkheid voor een bevoegde instantie om een nieuw bericht te sturen naar een andere bevoegde instantie.</p>
<p>2.    Maximaal aantal bijlagen verhogen naar 20 stuks (met een max van 1,5 GB in totaal). </p>
