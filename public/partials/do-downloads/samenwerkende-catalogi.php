<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>Samenwerkende Catalogi is een verwijsmechanisme voor productinformatie van overheidsorganisaties (zowel lokaal, regionaal als landelijk). Deelnemende organisaties publiceren enerzijds metadata over hun producten conform de Standaard voor Samenwerkende Catalogi. Anderzijds tonen zij de producten van andere organisaties op hun eigen website. Dit verwijsmechanisme wordt gefaciliteerd door de zoekdienst voor Samenwerkende Catalogi. Behalve op de websites van deelnemende organisaties wordt de informatie uit Samenwerkende Catalogi ook getoond op de centrale portalen www.overheid.nl en www.antwoordvoorbedrijven.nl.</p>    <p>
        <em>Datum laatste wijziging: 15 januari 2015</em>
    </p>
