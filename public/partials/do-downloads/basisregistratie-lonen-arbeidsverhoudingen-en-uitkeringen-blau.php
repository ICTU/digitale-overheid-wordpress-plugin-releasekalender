    <h2 id="omschrijving">Omschrijving</h2>
    <p><abbr title="Basisregistratie Lonen Arbeidsverhoudingen en Uitkeringen">BLAU</abbr> zal bestaan uit een kernset van de gegevens uit de huidige Polisadministratie van het <abbr title="Uitvoeringsinstituut Werknemersverzekeringen">UWV</abbr>. De polisadministratie is reeds operationeel en gevuld. Zij het niet met de wettelijke status van een basisregistratie (de planning van de Polisadministratie is daarom niet opgenomen in de releasekalender). Gegevens uit de Polisadministratie kunnen echter wel geleverd worden aan overheidsorganisaties (voor zover wetgeving dat toestaat). Wat betreft planning bevindt <abbr title="Basisregistratie Lonen Arbeidsverhoudingen en Uitkeringen">BLAU</abbr> zich nog in een pril stadium. Er is nog geen besluit genomen of de basisregistratie <abbr title="Basisregistratie Lonen Arbeidsverhoudingen en Uitkeringen">BLAU</abbr> onwikkeld wordt.</p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
    </p>
