    <p>Digipoort is een centrale voorziening in de infrastructuur van de e-overheid voor de geautomatiseerde (system-to-system) informatie-uitwisseling tussen bedrijven en overheden. In plaats van vele afzonderlijke verbindingen tussen bedrijven en overheden sluit elke organisatie aan op Digipoort. Daarna is in principe communicatie met alle aangesloten partijen mogelijk.</p>
<p>Om informatie uit te kunnen wisselen moeten (in deze volgorde):1. de overheid (proces- of berichtstroomeigenaar) klaar zijn voor het ontvangen en verwerken van berichten, en een aansluiting op Digipoort realiseren,2. de berichtenstroom of het informatie-uitwisselingsproces worden geïmplementeerd op Digipoort,3. bedrijven aansluiten op Digipoort en per proces of berichtenstroom hun software gereed (laten) maken voor het versturen en ontvangen van berichten.</p>
<p>De koppelvlakken SMTP, POP3 en FTP zijn bedoeld voor de betrouwbare en veilige uitwisseling van berichten over eenvoudige en robuuste koppelvlakken.</p>	<h2>Gerealiseerde releases</h2>
