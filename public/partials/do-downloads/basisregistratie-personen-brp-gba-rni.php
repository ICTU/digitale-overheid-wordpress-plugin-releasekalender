    <h2 id="omschrijving">Omschrijving</h2>
    <p>
De Basisregistratie Personen (<abbr title="Basisregistratie Personen">BRP</abbr>) is een basisregistratie in ontwikkeling (programma Modernisering <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr>). De <abbr title="Basisregistratie Personen">BRP</abbr> bevat persoonsgegevens over alle ingezetenen van Nederland en over personen die niet in Nederland wonen - of hier slechts kort verblijven - maar die een relatie hebben met de Nederlandse overheid, de 'niet-ingezeten'. 
 
De <abbr title="Basisregistratie Personen">BRP</abbr> wordt gebaseerd op twee bestaande administraties:
<abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> (Gemeentelijke Basisadministratie Persoonsgegevens): Gegevens over personen ingeschreven bij een Nederlandse gemeente. De <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> heeft op dit moment al de status van een basisregistratie;
<abbr title="Registratie Niet Ingezetenen">RNI</abbr> (Registratie Niet-Ingezetenen). Gegevens over personen die niet in Nederland wonen, maar wel een relatie met de Nederlandse overheid hebben. Zoals grensarbeiders, buitenlandse studenten en 'pensionado's'. De defitieve invoeringsdatum van de <abbr title="Registratie Niet Ingezetenen">RNI</abbr> is afhankelijk van de inwerkingtreding van de nieuwe wet basisregistratie personen.Daarnaast is er nog de PIVA Verstrekkingen. Deze bevat gegevens over personen ingeschreven op de BES-eilanden (Bonaire, Sint Eustatius en Saba). De persoonslijsten in de PIVA-V en de persoonslijsten in de <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> komen grotendeels overeen, maar er zijn enkele verschillen. In het document ‘<a href="http://www.bprbzk.nl/dsresource?objectid=33216&amp;type=org" class="external_link">Verschillenmatrix persoonslijst <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> versus PIVA</a>’ worden deze verschillen beschreven. Mogelijk zullen in de toekomst de BES-eilanden ook onderdeel uitmaken van de <abbr title="Basisregistratie Personen">BRP</abbr>. Daar is echter nog geen besluit over genomen.</p>    <p>
        <em>Datum laatste wijziging: 11 oktober 2016</em>
