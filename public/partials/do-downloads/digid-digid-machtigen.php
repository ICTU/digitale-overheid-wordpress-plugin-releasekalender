    <p>Met <abbr title="Digitale Identiteit">DigiD</abbr> Machtigen kunnen burgers hun overheidszaken via internet door iemand anders laten regelen. Dit is handig als ze hier zelf niet toe in staat zijn. Of ze vinden het makkelijk als iemand anders dit voor ze doet. Het afstaan van de persoonlijke <abbr title="Digitale Identiteit">DigiD</abbr> is hierbij niet nodig.</p>
    <p>Organisaties die gebruik mogen maken van <abbr title="Digitale Identiteit">DigiD</abbr> kunnen <abbr title="Digitale Identiteit">DigiD</abbr> Machtigen inzetten voor alle digitale diensten aan burgers waarvoor zij <abbr title="Digitale Identiteit">DigiD</abbr> inzetten.</p>
    <p>Sinds 2012 is met de invoering van de diensten catalogus het mogelijk om met één machtigingshandeling een burger voor meerdere diensten te machtigen.</p>
