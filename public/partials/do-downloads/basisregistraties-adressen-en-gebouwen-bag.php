    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Basisregistraties Adressen en Gebouwen (<abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr>) is de registratie waarin gemeentelijke basisgegevens over alle gebouwen en adressen in Nederland zijn verzameld. De <abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr> is gebaseerd op de Wet basisregistraties adressen en gebouwen. Gemeenten zijn bronhouder van de gebouw- en adresgegevens. Alle bestuursorganen zijn verplicht om vanaf 1 juli 2011 gebruik te maken van de <abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr> bij de uitvoering van hun publiekrechtelijke taken.</p>    <p>
