    <h2 id="omschrijving">Omschrijving</h2>
    <p></p>
<p>Overheidsinstellingen moeten op de hoogte zijn van gebeurtenissen die voor hun taken relevant zijn. Denk aan de geboorte van een persoon, het starten van een onderneming of het vaststellen van een inkomen. Basisregistraties kunnen gegevens over deze gebeurtenissen leveren. Afnemers van de basisregistraties kunnen zich met Digilevering abonneren op deze gebeurtenissen. </p>    <p>
        <em>Datum laatste wijziging: 22 juli 2016</em>
    </p>
