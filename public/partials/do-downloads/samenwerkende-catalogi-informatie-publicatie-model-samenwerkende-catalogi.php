    <p>Samenwerkende Catalogi is een standaard voor het publiceren en uitwisselen van metadata over producten- en dienstenbeschrijvingen van de overheid.</p>
<p>Voor elke product- of dienstenbeschrijving die deelnemers op hun eigen website publiceren wordt metadata gemaakt die wordt aangeboden aan en opgenomen in de collectie van Samenwerkende Catalogi. Deze collectie van metadata over producten en diensten van alle deelnemers is vervolgens doorzoekbaar via de zoekdienst van KOOP. </p>
<p>Samenwerkende Catalogi maakt gebruik van de zoekdienst van KOOP als ondersteunende voorziening voor de collectie. Bevraging van de collectie met de Zoekdienst van KOOP geschiedt op basis van Search / Retrieve via URL (SRU webservice):</p>
<ul><li>Open standaard</li>
<li>Zoekprotocol gebaseerd op XML</li>
<li>Gebruikt Contextual Query Language</li>
<li>Bevraging via request url</li>
