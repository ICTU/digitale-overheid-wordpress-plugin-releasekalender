    <p>TOP10NL is objectgericht. Het gegevensmodel ondersteunt dit. Elk geo-object is door middel van een uniek nummer te benaderen. Daarnaast kan ook de objectklasse afgebeeld worden. Elk afzonderlijk object uit het terrein met bepaalde unieke eigenschappen, bijv. een gebouw, een perceel bouwland, een deel van een weg (hier aangeduid als geo-object) is afzonderlijk te onderscheiden en heeft een unieke identificatiecode. Een geo-object heeft een bepaalde geometrie (punt , lijn of vlak) en wordt verder gekarakteriseerd door attributen en attribuutwaarden.</p>	<h2>Gerealiseerde releases</h2>
