    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Basisregistratie Grootschalige Topografie (<abbr title="Basisregistratie grootschalige Topografie">BGT</abbr>) wordt een uniform topografisch basisbestand met objecten in heel Nederlandop een schaal van 1:500 tot 1:5.000. Het doel van de realisatie van de <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr> is dat de hele overheid gebruik maakt van dezelfde basisset grootschalige topografie van Nederland. Topografie is de beschrijving van de fysieke werkelijkheid. Dus de dingen die in het terrein fysiek aanwezig zijn.</p>    <p>
        <em>Datum laatste wijziging: 17 februari 2016</em>
    </p>
