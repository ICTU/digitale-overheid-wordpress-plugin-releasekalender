    <p><abbr title="Management Overleg">MO</abbr>-DKK staat voor Massale Output Digitale Kadastrale Kaart.   Een Digitale Kadastrale Kaart geeft in één oogopslag de ligging van de Kadastrale percelen. De kaart toont perceelnummers, de belangrijkste bebouwing, straatnamen en huisnummers. De Kadastrale Kaart wordt veelal gebruikt in Geografische Informatiesystemen. (GIS systemen) De Digitale Kadastrale Kaart geeft slechts een indicatie van de ligging van de kadastrale grenzen, er mogen geen maten aan worden ontleend. Indien maten noodzakelijk zijn en gegevens over de exacte ligging van de kadastrale grens, dan is het noodzakelijk ter plaaste een grensreconstructie uit te laten voeren of om de aanwezige meetgegevens bij het Kadaster te raadplegen.</p>	<h2>Gerealiseerde releases</h2>
