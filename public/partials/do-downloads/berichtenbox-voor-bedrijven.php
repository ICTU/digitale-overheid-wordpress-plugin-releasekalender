    <h2 id="omschrijving">Omschrijving</h2>
    <p>De berichtenbox voor bedrijven is een beveiligd <abbr title="elektronische mail">e-mail</abbr>systeem waarmee u digitaal berichten kunt uitwisselen met Nederlandse overheidsinstanties (de Rijksoverheid, provincies, gemeenten en waterschappen). De berichtenbox is bedoeld voor ondernemers die gevestigd zijn in de Europese Economische Ruimte (EER) - dus ook in Nederland - en die hun diensten in Nederland willen aanbieden.Â De berichtenbox kan gebruikt worden voor procedures die onder de Dienstenwet vallen, en voor procedures waarbij de bevoegde instantie ervoor gekozen heeft deze (ook) via de berichtenbox te laten verlopen.</p>    <p>
        <em>Datum laatste wijziging: 26 juli 2016</em>
    </p>
