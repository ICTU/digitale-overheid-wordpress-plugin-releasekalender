    <p>TOP10NL is het digitale topografische bestand van het Kadaster dat bruikbaar is op schaalniveau tussen 1:5.000 en 1:25.000. TOP10NL is ontstaan vanuit luchtfoto’s, veldopnamen en reeds bestaande bestanden. De bijhouding vindt plaats door interpretatie van digitaal beeldmateriaal en gegevens uit externe bronnen. Het bestand is uniform en consistent. Ook is TOP10NL landsdekkend wat betekent dat u het als basis kunt gebruiken over heel Nederland.</p>	<h2>Gerealiseerde releases</h2>
