    <p>Persoonlijke Gegevens is één van de 3 functies van MijnOverheid. Het biedt burgers overzicht van, toeleiding naar en toegang tot hun persoonlijke gegevens bij de overheid. Het geeft hen inzicht hoe zij bij de overheid geregistreerd staan en helpt hen om onjuistheden in deze informatie te laten corrigeren.</p>
