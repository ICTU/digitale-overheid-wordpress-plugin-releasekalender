    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Basisregistratie Inkomen (<abbr title="Basisregistratie Inkomen">BRI</abbr>) bevat van circa dertien miljoen burgers het authentieke inkomensgegeven. Dit is het verzamelinkomen, of als dat er niet is, het belastbare jaarloon over het afgelopen kalenderjaar. En gegevens zoals het burgerservicenummer (<abbr title="Burgerservicenummer">BSN</abbr>).</p>    <p>
        <em>Datum laatste wijziging: 11 oktober 2016</em>
    </p>
