<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>In de Basisregistratie Voertuigen worden gegevens vastgelegd over voertuigen en de eigenaren daarvan. Uit de registratie verstrekt de Rijksdienst voor het Wegverkeer informatie aan burgers en bedrijven. De gegevens zijn landelijk beschikbaar voor overheidsinstanties, zoals de politie en de Belastingdienst.</p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
    </p>
