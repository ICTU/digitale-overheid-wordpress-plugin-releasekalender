<div id="kolom12">
    <h2 id="omschrijving">Omschrijving</h2>
    <p>Het burgerservicenummer (<abbr title="Burgerservicenummer">BSN</abbr>) is het persoonsnummer voor contacten met de overheid. Dit unieke nummer helpt om bijvoorbeeld persoonsverwisselingen te voorkomen. Iedereen die zich voor het eerst inschrijft bij een gemeente krijgt een <abbr title="Burgerservicenummer">BSN</abbr>. Ook een pasgeboren kind krijgt bij de aangifte van de geboorte meteen een <abbr title="Burgerservicenummer">BSN</abbr>. Het <abbr title="Burgerservicenummer">BSN</abbr> staat op het paspoort, rijbewijs en de identiteitskaart.</p>    <p>
        <em>Datum laatste wijziging: 11 oktober 2016</em>
    </p>
