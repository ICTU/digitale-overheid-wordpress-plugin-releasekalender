    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Basisregistratie Kadaster (<abbr title="Basisregistratie Kadaster">BRK</abbr>) bevat informatie over percelen, eigendom, hypotheken, beperkte rechten (zoals recht van erfpacht, opstal en vruchtgebruik) en leidingnetwerken. Daarnaast staan er kadastrale kaarten in met perceel, perceelnummer, oppervlakte, kadastrale grens en de grenzen van het rijk, de provincies en gemeenten.</p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
