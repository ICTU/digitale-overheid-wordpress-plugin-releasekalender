    <p>Hoeksteen van de PKI voor de overheid is het zogeheten Programma van Eisen (PvE). Dit programma is gebaseerd op Europese standaarden en Nederlandse wetgeving. Hiermee kunnen gebruikers er op vertrouwen dat zij gebruikmaken van een kwalitatief hoogwaardige en betrouwbare PKI-infrastructuur, die tevens voldoet aan internationaal geaccepteerde richtlijnen. Het PvE stelt eisen aan CSP’s voor een veilige en betrouwbare uitgifte van <abbr title="Public Key Infrastructure voor de overheid">PKIoverheid</abbr> certificaten.</p>
