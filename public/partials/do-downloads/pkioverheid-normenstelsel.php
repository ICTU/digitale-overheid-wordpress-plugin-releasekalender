		    <p>Elk half jaar wordt een nieuwe versie van het PvE gepubliceerd.</p>
<p>De release van PvE versie 4.2 zal onder andere een eis bevatten hoe moet worden omgegaan met server signing met persoonsgebonden certificaten. D.w.z. het zetten van een gekwalificeerde handtekening met een persoonsgebonden certificaat dat op een veilig middel staat dat niet direct in het bezit is van de eigenaar, zoals een HSM op een server. Dit houdt ook in dat de sleutelparen van persoonsgebonden certificaten  door abonnees kunnen worden gegenereerd onder bepaalde, strenge condities.</p>
