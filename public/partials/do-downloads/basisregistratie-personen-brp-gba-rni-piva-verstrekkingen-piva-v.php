    <p>Om de gegevensverstrekking uit de bevolkingsadministraties van Caribisch Nederland zo effectief en efficiënt mogelijk te laten verlopen, is een centraal bestand opgebouwd met een kopie van alle persoonslijsten die ook in de drie PIVA’s van Caribisch Nederland aanwezig zijn. Deze zogenoemde ‘PIVA-V’ wordt beheerd door het Agentschap Basisadministratie Persoonsgegevens en Reisdocumenten (<abbr title="Basisadministratie Persoonsgegevens en Reisdocumenten">BPR</abbr>). PIVA-V staat voor PIVA Verstrekkingen. PIVA-V wordt geactualiseerd vanuit de PIVA-systemen van Caribisch Nederland. Elke werkdag worden daarvoor alle persoonslijsten (PL-en) uit de PIVA’s van Caribisch Nederland opgehaald. Een online service zorgt voor de verstrekking vanuit PIVA-V aan gebruikers van de PIVA-V.</p>	<h2>Gerealiseerde releases</h2>
