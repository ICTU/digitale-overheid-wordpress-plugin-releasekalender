    <h2 id="omschrijving">Omschrijving</h2>
    <p>De Basisregistratie Ondergrond (<abbr title="Basisregistratie Ondergrond">BRO</abbr>) moet gegevens gaan bevatten over de bodem en ondergrond, zoals bodemkundige en geologische opbouw van de ondergrond. Die gegevens zijn bijvoorbeeld van belang voor overheden die zich bezig houden met stijging van de zeewaterspiegel, bodemdaling, ondergronds bouwen, natuurbeheer, koude-warmteopslag of opslag van CO2.  N.B. De informatie op deze site wordt momenteel geactualiseerd. Binnenkort vindt u hier de geactualiseerde informatie over de planning van de <abbr title="Basisregistratie Ondergrond">BRO</abbr>. </p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
    </p>
