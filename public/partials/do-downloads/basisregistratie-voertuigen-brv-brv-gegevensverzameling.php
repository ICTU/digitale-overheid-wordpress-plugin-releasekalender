    <p>Sinds 1 juli 2008 fungeert het kentekenregister ook als 'Basisregistratie Voertuigen'. Hiermee wordt de registratie van belangrijke gegevens over voertuigen en hun eigenaren/houders onderdeel van het Stelsel van Basisregistraties in Nederland.Het kentekenregister van de RDW is al jaren een zeer betrouwbaar, volledig en actueel register van voertuiggegevens en gegevens van eigenaren/houders.   De Basisregistratie Voertuigen heeft een Koppeling met <abbr title="Gemeentelijke Basisadministratie Personen">GBA</abbr> <abbr title="Logisch Ontwerp">LO</abbr> 3.7 en HR versie 2010.   Als gevolg van diverse wijzigingen in de taken en rollen van de overheden binnen Nederland, vinden met grote regelmaat wijzigingen in het Kentekenregister plaats. Deze wijzigingen worden projectmatig doorgevoerd en gekoppeld aan inwerkingtreding van bijbehorende wetgeving.</p>	<h2>Gerealiseerde releases</h2>
