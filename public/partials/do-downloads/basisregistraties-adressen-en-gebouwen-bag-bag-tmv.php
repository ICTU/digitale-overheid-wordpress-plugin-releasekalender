		    <p>Het Kadaster realiseert een generiek terugmeldsysteem waarin de terugmeldingen op de <abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr>, maar ook andere basisregistraties kunnen worden beheerd. </p>
<p>Op 16 juni 2016 is het terugmeldsysteem in beheer genomen voor de <abbr title="Basisregistratie grootschalige Topografie">BGT</abbr>. Vanaf Q4 worden de terugmeldingen vanuit de <abbr title="Basisregistraties Adressen en Gebouwen">BAG</abbr> viewer opgeslagen in dit systeem. De verwachting is dat in Q1 2017 terugmeldingen ook afgehandeld kunnen worden door bronhouders. </p><h4>Afhankelijkheden</h4>
