    <h2 id="omschrijving">Omschrijving</h2>
    <p>In het Handelsregister staan alle ondernemers en rechtspersonen in Nederland geregistreerd. Ook groepen die voorheen geen inschrijfplicht hadden, zoals eenmanszaken in de landbouw en beoefenaars van vrije beroepen. Ook gemeenten zijn verplicht per 2010 in het Handelsregister te staan.</p>    <p>
        <em>Datum laatste wijziging: 25 juli 2016</em>
    </p>
