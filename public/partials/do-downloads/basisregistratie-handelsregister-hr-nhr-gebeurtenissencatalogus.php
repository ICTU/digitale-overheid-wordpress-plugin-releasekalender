    <p>De gebeurtenissencatalogus nieuw Handelsregister beschrijft de gebeurtenissen zoals deze in het Handelsregister worden opgenomen. Per gebeurtenis staat beschreven wat de inhoud van het bericht is, als deze gebeurtenis zich heeft voorgedaan en is ingevoerd in het Handelsregister. Gebeurtenissen zorgen uiteindelijk voor betere integratie van ketenprocessen tussen het Handelsregister als basisregistratie en haar afnemers. Afnemers kunnen op basis van deze gebeurtenissen nog beter gebruik maken van de informatie van het Handelsregister en hun processen efficienter inrichten.</p>	<h2>Gerealiseerde releases</h2>
