		    <p>Als gevolg van nieuwe functionaliteit in het Handelsregister Registratie Systeem, is een aantal nieuwe gebeurtenissen ontstaan. Deze nieuwe gebeurtenissen zijn opgenomen in deze nieuwe versie.  De nieuwe gebeurtenissen betreffen voornamelijk herstel- en correctiefunctionaliteit (zoals de gebeurtenis 'herstellen beeindiging vestiging' en 'corrigeren handelsnaam'). De overige nieuwe gebeurtenissen zijn het resultaat van de uitbreiding van de wijzigingsfunctionaliteit van het registratiesysteeem (bijv. 'wijzigen geplaatst kapitaal bij buitenlandse vennootschap'). </p>
